def Letter_Grade(grade):
    if grade >= 90:
     print('A+')
    elif grade <= 89 and grade >= 85:
        print('A')
    elif grade <= 84 and grade >= 80:
        print('A-')
    elif grade <= 79 and grade >= 77:
        print('B+')
    elif grade <= 76 and grade >= 73:
        print('B')
    elif grade <= 72 and grade >= 70:
        print('B-')
    elif grade < 70:
        print('F')

